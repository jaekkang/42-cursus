int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (nb < 0)
		return (0);
	if (nb < 2)
		return (nb);
	while (i * i < nb && i < 46340)
		i++;
	if (i == nb / i)
		return (i);
	return (0);
}
